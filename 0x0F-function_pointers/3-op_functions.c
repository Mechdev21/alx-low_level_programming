#include "3-calc.h"
/**
 * op_add - performs addition
 * @a: values
 * @b: values
 *
 * Return: additon of both
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - performs subtraction
 * @a: value
 * @b: value
 *
 * Return: subtraction of both
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - performs multiplication
 * @a: value
 * @b: value
 *
 * Return: multiplication of both
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - peforms division
 * @a: value
 * @b: value
 *
 * Return: division of both
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulus operation
 * @a: value
 * @b: value
 *
 * Return: modulus of a b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
